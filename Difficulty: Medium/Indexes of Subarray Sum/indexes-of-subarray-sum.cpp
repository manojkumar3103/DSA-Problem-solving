class Solution {
  public:
    vector<int> subarraySum(vector<int> &arr, int target) {
        vector<int> ans;
        int s=0,sum=0,i=0,n=arr.size();
        while(i<n)
        {
            sum+=arr[i];
            while(sum>target)
            {
                sum-=arr[s];
                s++;
            }
            if(sum==target)
                return {s+1,i+1};
            i++;
        }
        return {-1};
    }
};

